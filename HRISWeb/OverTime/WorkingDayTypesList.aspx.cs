        protected void btnAccept_ServerClick(object sender, EventArgs e)
        {
            WorkingDayTypesEntity workingDayTypes = new WorkingDayTypesEntity();
            try
            {

                workingDayTypes.GeographicDivisionCode = SessionManager.GetSessionValue<DivisionByUserEntity>(SessionKey.WorkingDivision).GeographicDivisionCode;
                workingDayTypes.DivisionCode = SessionManager.GetSessionValue<DivisionByUserEntity>(SessionKey.WorkingDivision).DivisionCode;
                workingDayTypes.WorkingDayTypeCode = string.IsNullOrEmpty(txtWorkingDayTypeCode.Text) ? 0 : Convert.ToInt32(txtWorkingDayTypeCode.Text);
                workingDayTypes.WorkingDayTypesName = txtWorkingDayTypesName.Text;
                bool isSuccess = false;

                if (workingDayTypes.WorkingDayTypeCode.Equals(0))
                {
                    workingDayTypes.CreatedBy = UserHelper.GetCurrentFullUserName;
                    isSuccess = workingDayTypesBLL.AddWorkingDayTypes(workingDayTypes);
                }
                else
                {
                    workingDayTypes.LastModifiedUser = UserHelper.GetCurrentFullUserName;
                    isSuccess = workingDayTypesBLL.UpdateWorkingDayTypes(workingDayTypes);
                }

                if (isSuccess)
                {
                    SearchResults(PagerUtil.GetActivePage(blstPager));
                    MensajeriaHelper.MostrarMensaje(Page, TipoMensaje.Informacion, Convert.ToString(GetLocalResourceObject("msjSaveRecordSuccess")));
                    ScriptManager.RegisterStartupScript((Control)sender, sender.GetType(), Format("ReturnFromBtnAcceptClickPostBack{0}", Guid.NewGuid()), "setTimeout(function () {{ ReturnFromBtnAcceptClickPostBackError(); }}, 200);", true);
                    ScriptManager.RegisterStartupScript(this, this.GetType(), "HidePopup", "$('#MaintenanceDialog').modal('hide')", true);
                    return;
                }
            }
            catch (Exception ex)
            {
                
                if (ex is DataAccessException
                    || ex is BusinessException
                    || ex is PresentationException)
                {
                    MensajeriaHelper.MostrarMensaje(Page, TipoMensaje.Error, ex.Message);
                }
                else
                {
                    PresentationException newEx = new PresentationException(Convert.ToString(GetLocalResourceObject("msj001.Text")), ex);
                    MensajeriaHelper.MostrarMensaje(Page, TipoMensaje.Error, newEx.Message);
                }
                ScriptManager.RegisterStartupScript((Control)sender, sender.GetType(), Format("ReturnFromBtnAcceptClickPostBack{0}", Guid.NewGuid()), "setTimeout(function () {{ ReturnFromBtnAcceptClickPostBackError(); }}, 200);", true);
            }
            finally
            {

                PagerUtil.SetActivePage(blstPager, PagerUtil.GetActivePage(blstPager));

                
            }
        }
