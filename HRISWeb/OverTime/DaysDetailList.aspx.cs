        /// <summary>
        /// Day Types DropDown
        /// </summary>
        /// <param name="dropDownList">pass dropdown name</param>
        private void BindDayTypes(DropDownList dropDownList)
        {
            List<DayTypesEntity> dayTypesEntities = new List<DayTypesEntity>();
            try
            {
                dayTypesEntities.Add(new DayTypesEntity() { DayTypeCode = -1, DayTypesName = "" });
                dayTypesEntities.AddRange(dayTypesBLL.GetDayTypesListForDropdown(SessionManager.GetSessionValue<DivisionByUserEntity>(SessionKey.WorkingDivision).GeographicDivisionCode, SessionManager.GetSessionValue<DivisionByUserEntity>(SessionKey.WorkingDivision).DivisionCode));
                dropDownList.DataSource = dayTypesEntities;
                dropDownList.DataValueField = "DayTypeCode";
                dropDownList.DataTextField = "DayTypesName";
                dropDownList.DataBind();
            }
            catch (Exception ex)
            {
                if (ex is DataAccessException
                    || ex is BusinessException
                    || ex is PresentationException)
                {
                    MensajeriaHelper.MostrarMensaje(uppActions, TipoMensaje.Error, ex.Message);
                }
                else
                {
                    PresentationException newEx = new PresentationException(Convert.ToString(GetLocalResourceObject("msj001.Text")), ex);
                    MensajeriaHelper.MostrarMensaje(uppActions, TipoMensaje.Error, newEx.Message);
                }
            }
            finally
            {
                PagerUtil.SetActivePage(blstPager, PagerUtil.GetActivePage(blstPager));
            }
        }
