        /// <summary>
        /// Handles the btnAccept click event
        /// </summary>
        /// <param name="sender">Refers to the object that invoked the event that fired the event handle</param>
        /// <param name="e">Contains the event data</param>
        protected void btnAccept_ServerClick(object sender, EventArgs e)
        {
            DaysDetailEntity daysDetailEntity = new DaysDetailEntity();
            try
            {
                daysDetailEntity.GeographicDivisionCode = SessionManager.GetSessionValue<DivisionByUserEntity>(SessionKey.WorkingDivision).GeographicDivisionCode;
                daysDetailEntity.DivisionCode = SessionManager.GetSessionValue<DivisionByUserEntity>(SessionKey.WorkingDivision).DivisionCode;
                daysDetailEntity.DaysDetailCode = string.IsNullOrEmpty(txtDaysDetailCode.Text) ? 0 : Convert.ToInt32(txtDaysDetailCode.Text);
                daysDetailEntity.DayTypeCode = string.IsNullOrEmpty(cboDayTypeCode.SelectedValue) ? 0 : Convert.ToInt32(cboDayTypeCode.SelectedValue);
                daysDetailEntity.DescriptionDay = txtDescriptionDay.Text;
                daysDetailEntity.CodeDateBase = string.IsNullOrEmpty(dtpCodeDateBase.Text) ? new DateTime(1, 1, 1) : DateTime.ParseExact(String.Format("{0} 00:00", dtpCodeDateBase.Text), "MM/dd/yyyy HH:mm", CultureInfo.InvariantCulture, DateTimeStyles.None);
                bool isSuccess = false;


                if (daysDetailEntity.DaysDetailCode.Equals(0))
                {
                    daysDetailEntity.CreatedBy = UserHelper.GetCurrentFullUserName;
                    isSuccess = daysDetailBLL.AddDaysDetail(daysDetailEntity);
                }
                else
                {
                    daysDetailEntity.LastModifiedUser = UserHelper.GetCurrentFullUserName;
                    isSuccess = daysDetailBLL.UpdateDaysDetail(daysDetailEntity);
                }

                if (isSuccess)
                {
                    SearchResults(PagerUtil.GetActivePage(blstPager));
                    MensajeriaHelper.MostrarMensaje(Page, TipoMensaje.Informacion, Convert.ToString(GetLocalResourceObject("msjSaveRecordSuccess")));
                    ScriptManager.RegisterStartupScript(this, this.GetType(), "HidePopup", "$('#MaintenanceDialog').modal('hide')", true);
                    return;
                }
                else
                {
                    MensajeriaHelper.MostrarMensaje(Page, TipoMensaje.Error, Convert.ToString(GetLocalResourceObject("msgDublicateRecord")));
                }
            }
            catch (Exception ex)
            {
                
                if (ex is DataAccessException
                    || ex is BusinessException
                    || ex is PresentationException)
                {
                    MensajeriaHelper.MostrarMensaje(Page, TipoMensaje.Error, ex.Message);
                }
                else
                {
                    PresentationException newEx = new PresentationException(Convert.ToString(GetLocalResourceObject("msj001.Text")), ex);
                    MensajeriaHelper.MostrarMensaje(Page, TipoMensaje.Error, newEx.Message);
                }
                ScriptManager.RegisterStartupScript((Control)sender, sender.GetType(), Format("ReturnFromBtnAcceptClickPostBack{0}", Guid.NewGuid()), "setTimeout(function () {{ ReturnFromBtnAcceptClickPostBackError(); }}, 200);", true);
            }
            finally
            {

                PagerUtil.SetActivePage(blstPager, PagerUtil.GetActivePage(blstPager));

                
            }
        }
